package dev;

public class Ac_Holder {
    private String f_name;
    private String l_name;

    public Ac_Holder(String f_name, String l_name ){
        this.f_name=f_name;
        this.l_name=l_name;
    }

    public String getF_name() {
        return f_name;
    }

    public String getL_name() {
        return l_name;
    }
}
